//Logical AND
function logicalAND1(){
    window.alert("true & true = true")
    }
    function logicalAND2(){
    window.alert("true & false = false")
    }
    function logicalAND3(){
    window.alert("false & true = false")
    }
    function logicalAND4(){
    window.alert("false & false = false")
    }
    
    //Logical OR
    function logicalOR1(){
    window.alert("true & true = true")
    }
    function logicalOR2(){
    window.alert("true & false = true")
    }
    function logicalOR3(){
    window.alert("false & true = true")
    }
    function logicalOR4(){
    window.alert("false & false = false")
    }
    
    //Logical Not
    function logicalNOT1(){
    window.alert("!true = false")
    }
    function logicalNOT2(){
    window.alert("!false = true")
    }